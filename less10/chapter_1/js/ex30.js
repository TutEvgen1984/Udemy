    const startDate = new Date()
    const endDate = new Date(startDate.getTime() + seconds*1000)
    // –ì–¥–µ startDate.getTime() - –ø–µ—Ä–µ–≤–æ–¥–∏–º startDate –≤ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥—ã Timestamp
    // seconds*1000 : –ø–µ—Ä–µ–≤–æ–¥–∏–º —Å–µ–∫—É–Ω–¥—ã –≤ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥—ã –ø–æ—Ç–æ–º—É —á—Ç–æ –ø—Ä–µ–¥—ã–¥—É—â–µ–µ —Å–ª–∞–≥–∞–µ–º–æ–µ –≤ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥–∞—Ö

