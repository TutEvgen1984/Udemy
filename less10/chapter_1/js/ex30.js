    const timerFill = document.querySelector('.arsakaev__fill')
    const timerEmpty = document.querySelector('.arsakaev__empty')

    // –¢–∞–∫ –∫–∞–∫ –≤ —Å—Ç–∏–ª—è—Ö –∑–∞–¥–∞–Ω–æ —á—Ç–æ–±—ã –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –ø—Ä–æ–≥—Ä–µ—Å—Å–∞ –Ω–µ –±—ã–ª–æ –≤–∏–¥–Ω–æ —Å –ø–æ–º–æ—â—å—é "display: none;", —Ç–æ –Ω—É–∂–Ω–æ –µ–≥–æ –ø–æ–∫–∞–∑–∞—Ç—å
